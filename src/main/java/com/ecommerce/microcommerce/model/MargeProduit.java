package com.ecommerce.microcommerce.model;

public class MargeProduit {
	
	private String product;
	private int marge;
	
	
	public MargeProduit() {
	}
	
	
	public String getProduct() {
		return product;
	}
	public void setProduct(String product) {
		this.product = product;
	}
	public int getMarge() {
		return marge;
	}
	public void setMarge(int marge) {
		this.marge = marge;
	}
	
	
	

}
